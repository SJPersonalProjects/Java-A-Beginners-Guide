package java_a_beginners_guide.chapter_two;

public class CharacterAirthmeticDemo {
    public static void main(String[] args) {
        char character;

        character = 'X';
        System.out.println("Character is : " + character);

        character++; //character is incremented now.
        System.out.println("Character is : " + character);

        character = 90;
        System.out.println("Character is : " + character);
    }
}
